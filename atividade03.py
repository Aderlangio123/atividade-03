# Crie um programa que calcule a área de um retângulo, pedindo ao usuário a largura e a altura.

valorLargura = int(input("digite a largura do retângulo!  " ))
valorAltura = int(input("digite a altura do retângulo!  "))

areaRetângulo = valorLargura * valorAltura

print( "a area do retângulo é ", areaRetângulo)